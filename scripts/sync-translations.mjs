// --- Process files ---
function processLocale(filePath, translationMap) {
  const data = readJSON(filePath);
  const t = data.translation;
  
  for (const [flatKey, value] of Object.entries(translationMap)) {
    deepSet(t, flatKey, value);
  }
  
  // Update version
  t.settings.about['version-number'] = '3.0.5';
  
  writeFileSync(filePath, JSON.stringify(data, null, 4), 'utf8');
  
  // Verify
  const verify = readJSON(filePath);
  const enData = readJSON('./src/lib/stores/translations/en.json');
  const enKeys = new Set(flattenKeys(enData.translation));
  const localeKeys = new Set(flattenKeys(verify.translation));
  const missing = [...enKeys].filter(k => !localeKeys.has(k));
  console.log(`${filePath}: ${localeKeys.size} keys, ${missing.length} missing`);
  if (missing.length > 0) {
    console.log('  Still missing:', missing.slice(0, 10).join(', '), missing.length > 10 ? `... +${missing.length - 10}` : '');
  }
}

processLocale('./src/lib/stores/translations/uk.json', ukMap);
processLocale('./src/lib/stores/translations/zh-CN.json', zhMap);

console.log('\nDone!');
